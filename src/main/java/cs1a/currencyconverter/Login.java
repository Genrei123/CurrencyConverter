    private void convert_btnActionPerformed(java.awt.event.ActionEvent evt) throws IOException {//GEN-FIRST:event_convert_btnActionPerformed
        // TODO add your handling code here:
        String text = from_value.getText();

        float from = Float.parseFloat(text);
        String from_currency = from_box.getSelectedItem().toString();


        String to_currency = to_box.getSelectedItem().toString();

        String url_str = "https://api.exchangerate.host/latest?base=" + from_currency;

        OkHttpClient client = new OkHttpClient();
        Request request = new Request.Builder().url(url_str).get().build();

        Response response = client.newCall(request).execute();
        String stringResponse = response.body().string();
        JSONObject jsonObject = new JSONObject(stringResponse);
        JSONObject ratesObject = jsonObject.getJSONObject("rates");

        float rate = ratesObject.getFloat(to_currency);

        float result = Math.round(rate * from);

        try {

            Class.forName("com.mysql.cj.jdbc.Driver");

            // Establish a connection to the database


            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/transactions", "root", "");
            LocalDateTime now = LocalDateTime.now();
            // Prepare an SQL statement for insertion
            PreparedStatement stmt = conn.prepareStatement("INSERT INTO log (Name, Date, `From Currency`, `From Conversion`, `To Currency`, `To Conversion`) VALUES (?, ?, ?, ?, ?, ?)");

            // Retrieve the values of the registration form's text fields and insert them into the SQL statement
            stmt.setString(1, logintxt.getText());
            stmt.setString(2, String.valueOf(now));
            stmt.setString(3, from_currency);
            stmt.setString(4, String.valueOf(from));
            stmt.setString(5, to_currency);
            stmt.setString(6, String.valueOf(result));

            // Execute the SQL statement
            int rowsAffected = stmt.executeUpdate();

            if (rowsAffected > 0) {
                System.out.println("Transaction of: " + logintxt.getText() + " is successfully created");
                JOptionPane.showMessageDialog(null, from_currency + from + " is " + to_currency + result, "Success!", JOptionPane.INFORMATION_MESSAGE);
            } else {

                JOptionPane.showMessageDialog(this, "Conversion failed.");
            }

            stmt.close();
            conn.close();
        }

        catch (SQLException ex) {
            // Handle database connection errors
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database connection error.");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_convert_btnActionPerformed

