    private void DownloadActionPerformed(java.awt.event.ActionEvent evt) throws IOException {//GEN-FIRST:event_DownloadActionPerformed

        try {

            //Counts the rows and columns of the jTable
            int rowCount = jTable2.getRowCount();
            int colCount = jTable2.getColumnCount();

            // Create a new XSSFWorkbook object
            XSSFWorkbook workbook = new XSSFWorkbook();

            // Create a new sheet in the workbook
            XSSFSheet sheet = workbook.createSheet("Table Data");


            // Create a new Row and Cell for the headers
            Row headerRow = sheet.createRow(0);
            for (int i = 0; i < colCount; i++)
            {
                Cell headerCell = headerRow.createCell(i);
                headerCell.setCellValue(jTable2.getColumnName(i));
            }

            //Iterate over the rows and columns of the JTable, and add the data to the Excel sheet
            for (int i = 0; i < rowCount; i++) {
                Row row = sheet.createRow(i + 1);
                for (int j = 0; j < colCount; j++) {
                    Cell cell = row.createCell(j);
                    cell.setCellValue(jTable2.getValueAt(i, j).toString());
                }
            }

            //Prompts the user for directory
            JFileChooser fileChooser = new JFileChooser();
            fileChooser.setDialogTitle("Export Table");
            JFrame frame = new JFrame ("Export Data");
            int userSelection = fileChooser.showSaveDialog(frame);

            if (userSelection == JFileChooser.APPROVE_OPTION) {
                File fileToSave = fileChooser.getSelectedFile();
                String filePath = fileToSave.getAbsolutePath() + ".xlsx";

                // Write the workbook to an Excel file
                FileOutputStream outputStream = new FileOutputStream(filePath);
                workbook.write(outputStream);
                workbook.close();

                //Success message
                JOptionPane.showMessageDialog(frame, "Data named: " + fileToSave.getName() + " is exported.");
                frame.dispose();
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }




    } //GEN-LAST:event_DownloadActionPerformed



